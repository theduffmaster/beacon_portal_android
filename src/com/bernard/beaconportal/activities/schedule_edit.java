package com.bernard.beaconportal.activities;

public class schedule_edit {
	private String band;
	private String classes;

	public schedule_edit(String band, String classes) {

		super();
		this.band = band;
		this.classes = classes;

	}

	public String Band() {
		return band;

	}

	public String Classes() {
		return classes;

	}

}
