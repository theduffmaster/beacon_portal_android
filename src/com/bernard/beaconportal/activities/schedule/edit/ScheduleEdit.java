package com.bernard.beaconportal.activities.schedule.edit;

public class ScheduleEdit {
	private String band;
	private String classes;

	public ScheduleEdit(String band, String classes) {

		super();
		this.band = band;
		this.classes = classes;

	}

	public String Band() {
		return band;

	}

	public String Classes() {
		return classes;

	}

}
